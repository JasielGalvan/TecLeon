    private void btn_RegDocenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RegDocenteActionPerformed
        SqlDocente modSql = new SqlDocente();
        Docente doc = new Docente();
        
        String pass = new String(doc_Password.getPassword());
        String passCon = new String(doc_Spassword.getPassword());
        
        if ("".equals(doc_Clave.getText()) ||"".equals(doc_Nombre.getText()) || 
            "".equals(doc_Papellido.getText()) || 
            "".equals(doc_Sapellido.getText()) ||
            "".equals(doc_Domicilio.getText()) || "".equals(doc_Telefono.getText()) || 
            "".equals(doc_Correo.getText()) || doc_Nacimiento.getDate()==null){
            JOptionPane.showMessageDialog(null, "Verifique que todos los campos"
                    + " esten correctos.");
        }else{
            if(pass.equals(passCon)){
                SimpleDateFormat Date_Format = new SimpleDateFormat("yyyy-MM-dd");

                String nuevoPass = MD5.getHash(pass);

                doc.setId(Integer.parseInt(doc_Clave.getText()));
                doc.setNombre(doc_Nombre.getText());
                doc.setApe_Paterno(doc_Papellido.getText());
                doc.setApe_Materno(doc_Sapellido.getText());
                doc.setFechaNacimiento(Date_Format.format(doc_Nacimiento.getDate()));
                doc.setDireccion(doc_Domicilio.getText());
                doc.setTelefono(doc_Telefono.getText());
                doc.setCorreo(doc_Correo.getText());
                doc.setContrasena(nuevoPass);
                if (modSql.registrar(doc)){
                    JOptionPane.showMessageDialog(null, "REGISTRO GUARDADO");
                    limpiarFields();
                }else
                    JOptionPane.showMessageDialog(null, "Ha ocurrido un error, "
                            + "comprueba que los datos esten correctos.");
            }else{
                JOptionPane.showMessageDialog(null, "No coinciden las contraseñas");
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btn_RegDocenteActionPerformed

