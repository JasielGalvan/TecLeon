    private void alu_ClaveKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_alu_ClaveKeyTyped
        char c = evt.getKeyChar();
        int limite_carac=8;
        
        if (!(Character.isDigit(c) ||
            (c == evt.VK_BACK_SPACE) ||
            (c == evt.VK_DELETE))) {
            getToolkit().beep();
            evt.consume();
        }
        
        if(alu_Clave.getText().length()>=limite_carac){
            evt.consume();
            JOptionPane.showMessageDialog(null, "Solo se pueden ingresar 8 "
                    + "dígitos");
        }
    }//GEN-LAST:event_alu_ClaveKeyTyped

