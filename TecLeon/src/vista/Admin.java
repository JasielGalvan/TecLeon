    private void btn_ADocenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ADocenteActionPerformed
        String campo = doc_TClave.getText();
                
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            jtDocente.setModel(modelo);
            
            PreparedStatement ps;
            ResultSet rs;
            Conexion conn= new Conexion();
            Connection con = conn.getConexion();
            
            String sql ="SELECT idDocente, nombre, ape_paterno, ape_materno, "
                    +"fechaNacimiento, direccion, telefono, correo FROM Docente";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            
            ResultSetMetaData rsMd = rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
            
            modelo.addColumn("Clave");
            modelo.addColumn("Nombre");
            modelo.addColumn("Apellido Paterno");
            modelo.addColumn("Apellido Materno");
            modelo.addColumn("Fecha de Nacimiento");
            modelo.addColumn("Dirección");
            modelo.addColumn("Teléfono");
            modelo.addColumn("Correo");
            
            int[] anchos = {40,90,90,90,80,100,50,70};
            for(int x=0; x< cantidadColumnas; x++)
                jtDocente.getColumnModel().getColumn(x).setPreferredWidth(anchos[x]);
            
            while(rs.next()){
                Object[] filas = new Object[cantidadColumnas];
                
                for(int i=0;i < cantidadColumnas; i++){
                    filas[i]= rs.getObject(i+1);
                }
                modelo.addRow(filas);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,"Se encontro un error y no es "
                    + "posible mostrar la información");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btn_ADocenteActionPerformed

