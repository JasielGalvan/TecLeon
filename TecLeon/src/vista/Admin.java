    private void btnTEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTEliminarActionPerformed
        
        PreparedStatement ps = null;
        try {
            Conexion conn= new Conexion();
            Connection con = conn.getConexion();
            if (jtAlumnos.getSelectedRow()>-1){
                int fila =jtAlumnos.getSelectedRow();
                String codigo = jtAlumnos.getValueAt(fila, 0).toString();

                ps = con.prepareStatement("DELETE FROM Alumno WHERE idAlumno=?");
                ps.setString(1, codigo);
                ps.execute();
                limpiarFields();
                JOptionPane.showMessageDialog(null, "Se ha eliminado "
                        + "el registro.");
                disableField();
            }else{
                JOptionPane.showMessageDialog(null, "No ha sido posible eliminar "
                    + "el registro.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "No ha sido posible eliminar "
                    + "el registro.");
        }
    }//GEN-LAST:event_btnTEliminarActionPerformed

