    private void btn_RegAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RegAlumnoActionPerformed
        SqlAlumno modSql = new SqlAlumno();
        Alumno alum = new Alumno();
        
        String pass = new String(alu_Password.getPassword());
        String passCon = new String(alu_Spassword.getPassword());
        
        if ("".equals(alu_Clave.getText()) ||"".equals(alu_Nombre.getText()) || 
            "".equals(alu_Papellido.getText()) || 
            "".equals(alu_Domicilio.getText()) || "".equals(alu_Tel.getText()) || 
            "".equals(alu_Correo.getText()) || alu_Nacimiento.getDate()==null){
            JOptionPane.showMessageDialog(null, "Verifique que todos los campos"
                    + " esten correctos.");
        }else{
            if(pass.equals(passCon)){
                SimpleDateFormat Date_Format = new SimpleDateFormat("yyyy-MM-dd");

                String nuevoPass = MD5.getHash(pass);

                alum.setId(Integer.parseInt(alu_Clave.getText()));
                alum.setNombre(alu_Nombre.getText());
                alum.setApe_Paterno(alu_Papellido.getText());
                alum.setApe_Materno(alu_Sapellido.getText());
                alum.setFechaNacimiento(Date_Format.format(alu_Nacimiento.getDate()));
                alum.setDireccion(alu_Domicilio.getText());
                alum.setTelefono(alu_Tel.getText());
                alum.setCorreo(alu_Correo.getText());
                alum.setContrasena(nuevoPass);
                alum.setSemestre(Integer.parseInt(alu_Semestre.getText()));
                if (modSql.registrar(alum)){
                    JOptionPane.showMessageDialog(null, "REGISTRO GUARDADO");
                    limpiarFields();
                }else
                    JOptionPane.showMessageDialog(null, "Ha ocurrido un error, "
                            + "comprueba que los datos esten correctos.");
            }else{
                JOptionPane.showMessageDialog(null, "No coinciden las contraseñas");
            }
        }
    }//GEN-LAST:event_btn_RegAlumnoActionPerformed

